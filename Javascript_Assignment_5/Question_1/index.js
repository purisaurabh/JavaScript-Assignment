// What is the difference between ++i and i++?

// ++i (pre-increment): This increments the value of i before its current value is used. The updated value is then returned.

let a = 5;
let aValue = ++i;
console.log(a);      // Output: 6
console.log(aValue); // Output: 6



// i++ (post-increment): This increments the value of i after its current value is used. The current value is returned before the increment.


let b = 5;
let bValue = i++;
console.log(b);      // Output: 6
console.log(bValue); // Output: 5

